<h1>Über <strong>Adults in the Zoom</strong></h1>

<p>
Als DiEM25 im Februar 2016 gegründet wurde, formulierte der Gründer Yanis Varoufakis einen mitreißenden Aufruf: „Wir kommen aus allen Teilen Europas (…) als engagierte Europäer zusammen, die entschlossen sind zu verhindern, dass ein ahnungsloses und zutiefst demokratieverachtendes EU-Establishment eine authentisch demokratische Europäische Union unmöglich macht. (…)
Wir sind inspiriert von einem Europa der Vernunft, Freiheit, Toleranz und Phantasie“. Zehntausende Menschen aus mehr als einem Dutzend europäischer Länder folgten diesem Aufruf und wurden Mitglieder der Bewegung DiEM25.</p>
<p>Ich war von Oktober 2017 bis Oktober 2020 in der Bewegung aktiv, u.a. als Vorstand des deutschen Wahlflügels, Mitglied des nationalen Kollektivs von DiEM25 in Deutschland und Ko-Autor eines zentralen Positionspapier der Progressiven Agenda. Im Oktober 2020 traten ich und der Rest des Bundeskollektiv zurück und ich verließ die Bewegung. Was war geschehen?</p>
<p>Die großen Ziele der Bewegung - die Befreiung Europas aus den Fängen der neoliberalen Eliten und der illiberalen Machthaber im Osten Europas und die Umsetzung eines <strong>Green New Deal for Europe</strong> - erscheinen nach wie vor in weiter Ferne. Eine Mobilisierung von hunderttausenden von EU-Bürger*innen hat nicht stattgefunden, stattdessen treten wichtige Mitglieder der Bewegung reihenweise zurück. Wer trägt dafür die Schuld?</p>
<p>In meiner Analyse werde ich zeigen, dass eine Bewegung wie DiEM25 nur dann erfolgreich sein kann, wenn sie die eigenen Versprechung von Transparenz, demokratischer Mitbestimmung, Grassroots-Organisation und internationaler Kooperation ernst nimmt.</p>

<p>Das Buch ist ein spannender Erfahrungsbericht aus dem Inneren einer der faszinierendsten Bewegungen des 21. Jahrhunderts und ein Handbuch für Alles was nach DiEM25 kommt.
</p>


